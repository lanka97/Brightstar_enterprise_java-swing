        // TODO add your handling code here:
        
        try{
            //Sold Toshiba machine quantity
            String sql  = "SELECT COUNT(Model) from customer_details where Model = 'Toshiba' ";
            
            PreparedStatement ps = DBConnection.getDBconnection().prepareStatement(sql);
            ResultSet result = ps.executeQuery();
            
            if(result.next()){
             String count = result.getString("COUNT(Model)");
             toshiba1.setText(count);      
            }
            
            
            //Sold RICOH machine quantity
            String sql2 = "SELECT COUNT(Model) from customer_details where Model = 'RICOH' ";
            
            PreparedStatement ps2 = DBConnection.getDBconnection().prepareStatement(sql2);
            ResultSet result2 = ps2.executeQuery();
            
              if(result2.next()){
             String count1 = result2.getString("COUNT(Model)");
             ricoht.setText(count1);      
            }
           
           
            
           String sum = " SELECT SUM(Price) FROM customer_details" ;
           
            PreparedStatement ps3 = DBConnection.getDBconnection().prepareStatement(sum);
            ResultSet result3 = ps3.executeQuery();
           
           if(result3.next()){
             String sum1 = result3.getString("SUM(Price)");
             income.setText("Rs. "+sum1+".00");      
            }
           
        }catch(Exception e){
              e.printStackTrace();
        }
