    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          
         DefaultTableModel dm = (DefaultTableModel) j1.getModel();
        dm.getDataVector().removeAllElements();
        revalidate();
        
        try {
            // TODO add your handling code here:
      
            String spare = "SELECT * FROM machinemg";
    
            PreparedStatement ps = DBConnect.connect().prepareStatement(spare);
            DefaultTableModel Model = (DefaultTableModel) j1.getModel(); 
            ResultSet result = ps.executeQuery();
            
            while(result.next()){
                String id= result.getString(1);
                String name= result.getString(2);
                String brand= result.getString(3);
                String model = result.getString(4);
                String type = result.getString(5);
                String unitprice= result.getString(6);
                String val1 = result.getString(7);
                String a_quantity = result.getString(8);
                
                
                Model.addRow(new String[]{
                      id,name,brand,model,type,unitprice,val1,a_quantity,
                });
                 
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(StockReport.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

