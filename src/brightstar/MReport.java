    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        InputStream input = null;
            try {
                BasicConfigurator.configure();
                String path = "F:\\project itp\\BrightStar\\src\\brightstar\\machinereport2.jrxml";
                input = new FileInputStream(new File(path));
                JRDesignQuery jrquery = new JRDesignQuery();
                jrquery.setText("Select * from machinemg");
                JasperDesign jasperDesign = JRXmlLoader.load(input);
                jasperDesign.setQuery(jrquery);
                JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
                JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, DBConnect.connect());
                JasperViewer viewer = new JasperViewer(jasperPrint, false);
                viewer.setVisible(true);

            } catch (FileNotFoundException ex) {
                ex.printStackTrace();
            } catch (JRException ex) {
            Logger.getLogger(MReport.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
                try {
                    input.close();
                } catch (IOException ex) {
                    ex.printStackTrace();
                }
            }  
    }//GEN-LAST:event_jButton1ActionPerformed


