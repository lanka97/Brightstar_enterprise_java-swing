    public static String getMonth(String string) {

        String month = string;

        String monthString = "";

        switch (month) {

            case "January":
                monthString = "01";
                break;
            case "February":
                monthString = "02";
                break;
            case "March":
                monthString = "03";
                break;
            case "April":
                monthString = "04";
                break;
            case "May":
                monthString = "05";
                break;
            case "June":
                monthString = "06";
                break;
            case "July":
                monthString = "07";
                break;
            case "August":
                monthString = "08";
                break;
            case "September":
                monthString = "09";
                break;
            case "October":
                monthString = "10";
                break;
            case "November":
                monthString = "11";
                break;
            case "December":
                monthString = "12";
                break;
            default:
                System.out.println("Invalid Month!");
        }

        return monthString;
    }

    public void getMonthlyReport(String month) {

        if (!month.equals("")) {

            InputStream input = null;
            try {
                BasicConfigurator.configure();
                String path = "D:\\PROJECTS\\NetBeans\\BrightStar\\src\\brightstar\\reports\\SalesReport.jrxml";
                input = new FileInputStream(new File(path));
                JRDesignQuery jrquery = new JRDesignQuery();
                jrquery.setText("Select * from sparepart_sale WHERE date LIKE '_____" + month + "%'");
                JasperDesign jasperDesign = JRXmlLoader.load(input);
                jasperDesign.setQuery(jrquery);
                JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
                JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, DBConnection.getDBconnection());
                JasperViewer viewer = new JasperViewer(jasperPrint, false);
                viewer.setVisible(true);

            } catch (FileNotFoundException ex) {
                ex.printStackTrace();
            } catch (SQLException ex) {
                ex.printStackTrace();
            } catch (JRException ex) {
                ex.printStackTrace();
            } catch (ClassNotFoundException ex) {
                ex.printStackTrace();
            } finally {
                try {
                    input.close();
                } catch (IOException ex) {
                    ex.printStackTrace();
                }
            }
        }else{
        
           JOptionPane.showMessageDialog(null, "Invalid Month!",
                    "Alert", JOptionPane.PLAIN_MESSAGE);
        }

    }


