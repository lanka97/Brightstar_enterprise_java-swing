    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        try {
            String PaymentId = JpaymentId.getText();
            String ComplainId = JcomplainId.getText();
            String CustomerName = JcustomerName.getText();
            String Email = Jemail.getText();
            double Amount = Double.parseDouble(Jamount.getText());
            
           
            String updatePaymentQuery = "UPDATE maintenancepaymentnew "
                    + "SET complainId = ?,customerName = ?,email = ?,amount = ? "
                    + "WHERE paymentId = ?";
           
            PreparedStatement ps = DBConnection.getDBconnection().prepareStatement(updatePaymentQuery);
       
            ps.setString(1, ComplainId);
            ps.setString(2, CustomerName);
            ps.setString(3, Email);
            ps.setDouble(4, Amount);
            //ps.setString(5, PaymentCompletedDate);
            ps.setString(5, PaymentId);
            
            
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Payment updated successfully!",
                    "Confirmation Message", JOptionPane.PLAIN_MESSAGE);
            this.setVisible(true);
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdatePayment.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UpdatePayment.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.setVisible(false);
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

